# Tuplas: Permite almacenar diferentes tipos de datos, cualquier objeto
# Son inmutables, una vez creada no se puede modificar,
# ni quitar ni agregar elementos.
my_tuple = (1, "String", True)
print(my_tuple)
# Mostrar posicion especifica de la tupla
print(my_tuple[0:2])
# En la tupla no se puede agregar ni quitar elementos
# Se utilizan cuando se necesite control total de tipos de datos constantes
