# Numeros , tipos de datos primitivos
number_one = 10
number_two = 4
# Suma
result = number_one + number_two
print("Resultado number_one+number_two", result)
# Resta
result = number_one - number_two
print("Resultado number_one-number_two", result)
# Multiplicacion
result = number_one * number_two
print("Resultado number_one*number_two", result)
# Division flotante
